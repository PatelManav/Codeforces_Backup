void Solve() {
	ll e = 0, ans = 0, c = 0, n = s[0].size();
	for (ll i = 0; i < n; i++) {
		c = (s[0][i] == '0') + (s[1][i] == '0');
		e += c;
		if (e >= 3)e -= 3, ans++;
		else e = c;
	}
	cout << ans;
}

