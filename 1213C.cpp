void Solve() {
	ll arr[11] = {0}, sum = 0;
	for (ll i = 1; i < 11; i++) arr[i] = (m * i) % 10, sum += arr[i];
	ll d = n / m;
	ll ans = 0;
	ans += sum * (d / 10);
	for (ll i = 1; i <= d % 10; i++) ans += arr[i];
	cout << ans << endl;
}

