void Solve() {
	ll delta = abs(a - b);
	if (!delta) {
		cout << 0 << endl;
		return;
	}
	for (ll i = 1;; i++) {
		ll val = i * (i + 1) / 2;
		if (val >= delta and val % 2 == delta % 2) {
			cout << i << endl;
			return;
		}
	}
}

