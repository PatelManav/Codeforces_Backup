void Solve() {
	string s;
	cin >> s;
	string o = s;
	sort(all(o));
	if (o == s)cout << s << endl;
	else {
		ll x = 0, y = 0;
		for (auto it : s) {if (it == '0')x++; else break;}
		for (ll i = n - 1; i >= 0; i--) {if (s[i] == '1')y++; else break;}
		x++;
		while (x--)cout << 0;
		while (y--)cout << 1;
		cout << endl;
	}
}

